# 1. Basic Maths (Jo hum bachpan se jaante hain)
print(15+6)  # Jodna (Addition) -> 21
print(15-6)  # Ghatana (Subtraction) -> 9
print(15*6)  # Guna (Multiplication) -> 90

# 2. Division ka asli khel:
print(15/6)  # Floating Point Division: Isse hamesha point mein answer aayega -> 2.5

# 3. Floor Division (//): 
# Ye point ke baad ki saari kachra (decimal) hata deta hai aur sirf poora number deta hai
print(15//6) # Output: 2 (Point ke baad ka .5 gayab kar diya)

# 4. Exponential Operator (**): 
# Iska matlab hai "Power". 5 ki power 3 (5 * 5 * 5)
print(5**3)  # Output: 125

# 5. Modulus Operator (%): 
# Ye sabse kaam ki cheez hai! Ye batata hai ki bhag (divide) karne ke baad "Bacha" kitna (Remainder)
print(6%3)   # 3 ekam 3, bache 2. Output: 2
